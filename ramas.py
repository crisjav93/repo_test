#Crear las ramas es ir desde un determinado punto y avanzar en paralelo,
#se utiliza para trabajar en distintas cosas de un mismo programa
#luego se vuelve a unir
#el codigo es "git branch "nombre representativo del proyecto"
#el comando *git branch -l* nos muestra el rama master y la rama que creamos
# para cambiar de rama es *git checkout "nombre_de_la_rama"

#en las ramas, al crear un archivo lo trabajo, gravo, tiro un git add, un git status para checkear
#lo commiteo
#Cuando tiro el git checkout master/main, el archivo desaparece.
#son versiones paralelas trabajando, en un mismo control de versión
# con git branch -g borro una rama (siempre desde la master/main)

#como fucionar las ramas: 
#el comando es *git merge "nombre del archivo"
#va a quedar como rama principal en la que esté parado(preferentemente la master)
